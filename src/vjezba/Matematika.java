package vjezba;

public class Matematika {
	public double mnozenje(double a, double b) {
		return a * b;
	}

	public double oduzimanje(double a, double b) {
		return a - b;
	}

	public double dijeljenje(double a, double b) {
		return a / b;
	}

	public double sabiranje(double a, double b) {
		return a + b;
	}
}
